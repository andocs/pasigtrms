            <h2><b>Transportation</b></h2>
            <em>
                <p>PUBLIC TRANSPORTATIONS For a reluctant tourist, the optimal way of going about the city may be hopping into a cab and allowing it to take you on a perfunctory tour. On the other hand, going by any of the public transports available affords you a full experience of Pasig City, teeming with culture and heritage.</p>
                <p>On EDSA, you can either take the bus – both northbound and southbound buses stop in front of Robinson’s Galleria, a perfect place to start – or the Metro Rail Transit. The MRT stops at the Ortigas Station from which Robinson’s Galleria is but a leisurely stroll. From Galleria, navigating the rest of Ortigas Center’s “jewel” streets on foot is best.</P>
                <p>The quintessentially Filipino ride known as the “jeepney” goes the Pasig-Quiapo (and vice versa) route, which has existed for decades, and travels from the western side of Shaw Boulevard. The modern and cooler spin-off of the jeepney – the “FX” - is available at a terminal on the north side of Robinson’s Galleria.</p>
                <p>Pasig City offers various modes of transportation including jeepneys, buses, tricycles, and taxis. Additionally, there are also shuttle services and ride-sharing options available.</p>
